package modelo;

public class Passagem {
	private String localDestino;
	private String diaDaViagem;
	private String horarioDaViagem;
	
	public String getLocalDestino() {
		return localDestino;
	}
	public void setLocalDestino(String localDestino) {
		this.localDestino = localDestino;
	}
	public String getDiaDaViagem() {
		return diaDaViagem;
	}
	public void setDiaDaViagem(String diaDaViagem) {
		this.diaDaViagem = diaDaViagem;
	}
	public String getHorarioDaViagem() {
		return horarioDaViagem;
	}
	public void setHorarioDaViagem(String horarioDaViagem) {
		this.horarioDaViagem = horarioDaViagem;
	}
	
	
	

}
