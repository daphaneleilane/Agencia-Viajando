package modelo;

public class CompanhiaAerea {

	private String nome;
	private String telefoneParaReserva;
	
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getTelefoneParaReserva() {
		return telefoneParaReserva;
	}
	public void setTelefoneParaReserva(String telefoneParaReserva) {
		this.telefoneParaReserva = telefoneParaReserva;
	}

	
}
